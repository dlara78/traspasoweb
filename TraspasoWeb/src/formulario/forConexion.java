    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed

        String servidor = String.valueOf(this.cmbServidor.getSelectedItem());
        String baseDatos = this.txtBaseDatos.getText();
        String usuario = this.txtUsuario.getText();
        String password = new String (this.passPassword.getPassword());
        String resumen = servidor + " " + baseDatos + " " + usuario + " " + password;
        this.txtResumen.setText(resumen);
        try {
            con=conexion.getConnection(servidor, baseDatos, usuario, password);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e);
        }
        
        
    }//GEN-LAST:event_btnConectarActionPerformed

